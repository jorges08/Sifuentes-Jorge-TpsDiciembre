    public void eliminarRegistro(){
        
        int filaSeleccionada=tablaAlumnos.getSelectedRow();
                   
        try{
            
            String SQL="delete from alumnos where idalumnos="+tablaAlumnos.getValueAt(filaSeleccionada,0);
            
            Statement st=con.createStatement();
            
            int n=st.executeUpdate(SQL);
            
            if (n>0){
                
                JOptionPane.showMessageDialog(null,"Registro Eliminado Correctamente");
            
            }
        
        }catch (Exception e){
            
            JOptionPane.showMessageDialog(null,"Error al Eliminar Registro"+ e.getMessage());
        
        }
        
    }
    
    
public void actualizarDatos(){
    
    try {
        
        String SQL="update alumnos set nombre=?,apellido=?,materia=?,nota1=?,nota2=?,nota3=?,promedio=?,estatus=? where idalumnos=?";
        
        int filaSeleccionada=tablaAlumnos.getSelectedRow();
        String dao=(String)tablaAlumnos.getValueAt(filaSeleccionada,0);
                
        PreparedStatement pst=con.prepareStatement(SQL);
        
        pst.setString(1, txtNombre.getText());
        pst.setString(2, txtApellido.getText());
        
        int seleccionado=cbMateria.getSelectedIndex();
        pst.setString(3,cbMateria.getItemAt(seleccionado));
        
        pst.setDouble(4, Double.parseDouble(txtNota1.getText()));
        pst.setDouble(5, Double.parseDouble(txtNota2.getText())); 
        pst.setDouble(6, Double.parseDouble(txtNota3.getText()));
        pst.setDouble(7, Double.parseDouble(txtPromedio.getText()));
        
        pst.setString(8, txtCondicion.getText());
        
        pst.setString(9, dao);
        pst.execute();
        
        JOptionPane.showMessageDialog(null,"Registro Editado Correctamente");
        
    
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null,"Error en la Edición del Registro "+e.getMessage());
    }


}    
    
public void insertarDatos(){
    
    try {
        
        String SQL="insert into alumnos(nombre,apellido,materia,nota1,nota2,nota3,promedio,estatus) values (?,?,?,?,?,?,?,?)";
        PreparedStatement pst=con.prepareStatement(SQL);
        
        pst.setString(1, txtNombre.getText());
        pst.setString(2, txtApellido.getText());
        
        int seleccionado=cbMateria.getSelectedIndex();
        pst.setString(3,cbMateria.getItemAt(seleccionado));
        
        pst.setDouble(4, Double.parseDouble(txtNota1.getText()));
        pst.setDouble(5, Double.parseDouble(txtNota2.getText())); 
        pst.setDouble(6, Double.parseDouble(txtNota3.getText()));
        pst.setDouble(7, Double.parseDouble(txtPromedio.getText()));
        
        pst.setString(8, txtCondicion.getText());
        
        pst.execute();
        
        JOptionPane.showMessageDialog(null,"Registro Exitoso");
        
    
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null,"Error de Registro "+e.getMessage());
    }


}
    
