public void filtrarDatos(String valor){

    String[] titulos={"ID ","Nombre","Apellido","Materia","1 Trim","2 Trim","3 Trim","Promedio","Condicion"};
    String[] registros=new String[9];
    
    DefaultTableModel modelo=new DefaultTableModel(null,titulos);
    
    String SQL="select * from alumnos where nombre like '%"+valor+"%'";
    
    try {
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery(SQL);
                
        while (rs.next()){
            
            registros[0]=rs.getString("idalumnos");
            registros[1]=rs.getString("nombre");
            registros[2]=rs.getString("apellido");
            registros[3]=rs.getString("materia");
            registros[4]=rs.getString("nota1");
            registros[5]=rs.getString("nota2");
            registros[6]=rs.getString("nota3");
            registros[7]=rs.getString("promedio");
            registros[8]=rs.getString("estatus");
            
            
            modelo.addRow(registros);
                  
        }
        
        tablaAlumnos.setModel(modelo);
        
    }catch (Exception e){
        
        JOptionPane.showMessageDialog(null, "Error al Mostrar Datos "+ e.getMessage());
        
    }
    
}  
    
    
