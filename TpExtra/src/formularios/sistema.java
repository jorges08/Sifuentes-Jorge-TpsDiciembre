    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
               
        //Habilita los botones
        btnGuardar.setEnabled(true); 

        
        nota1=Double.parseDouble(txtNota1.getText());
        nota2=Double.parseDouble(txtNota2.getText());
        nota3=Double.parseDouble(txtNota3.getText());
        
        suma=nota1+nota2+nota3;
        promedio=suma/3;
        
        String str = String.valueOf(promedio);
        int intNumber = Integer.parseInt(str.substring(0, str.indexOf('.')));
        
        txtPromedio.setText(String.valueOf(intNumber));
        
        if (promedio>=6) {
            txtCondicion.setText("Aprobado");
        } else {
            txtCondicion.setText("Desaprobado");

}
        
    }//GEN-LAST:event_jButton1ActionPerformed

